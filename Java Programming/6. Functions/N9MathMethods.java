public class N9MathMethods {
    
    public static void main(String[] args) {
        int num1 = 9, num2 = 16, num3 = 25;

        System.out.println(Math.min(num1, Math.min(num2, num3)));
        System.out.println(Math.max(num1, Math.max(num2, num3)));
        System.out.println(Math.sqrt(num1));
        System.out.println(Math.pow(num1, 2));
        System.out.println(Math.abs(-6.4));
    }
}
