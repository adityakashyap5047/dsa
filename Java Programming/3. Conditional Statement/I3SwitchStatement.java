public class I3SwitchStatement {
    
    public static void main(String[] args) {
        int num = 9;

        switch (num) {
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Burger");
                break;
            case 3:
                System.out.println("Pizza");
                break;
            default:
                System.out.println("You are dreaming");
                break;
        }
    }
}
