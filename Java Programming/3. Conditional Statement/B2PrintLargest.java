public class B2PrintLargest {
    
    public static void main(String[] args) {
        
        int A = 10;
        int B = 5;

        if (A >= B){
            System.out.println(A + " is larger than or equal to " + B);
        } else {
            System.out.println(A + " is smaller than " + B);
        }
    }
}
