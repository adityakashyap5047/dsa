public class C1Variables {
    
    public static void main(String[] args) {
        
        String name = "Aditya Kumar";
        int age = 19;
        double cgpa = 8.3;

        System.out.println(name + " is "+age+" years old with "+cgpa+" cgpa");

        age = 20;
        cgpa = 8.5;

        System.out.println(name + " is "+age+" years old with "+cgpa+" cgpa");
    }
}
